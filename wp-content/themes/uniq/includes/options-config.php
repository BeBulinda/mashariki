function uniq_boolean($value) {
	if(is_bool($value)) {
		return $value;
	} else {
		return false;
	}
}

function uniq_breadcrumb_char_choices($value='') {
	$choices = array('1','2','3');

	if( in_array($value, $choices)) {
		return $value;
	} else {
		return '1';
	}
}